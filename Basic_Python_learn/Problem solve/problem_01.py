# Write a Python program that takes two numbers as input from the user and performs the following
# operations:

# Addition
# Subtraction
# Multiplication
# Division
# Floor Division
# Modulus
# Exponentiation
# Print the results of each operation.

x=int(input("Enter First value:-"))
y=int(input("Enter second Value:-"))
print("Perform Basic Operations:-")
print("Addition=",x+y)
print("Subtraction=",x-y)
print("Multiplication=",x*y)
print("Division=",x/y)
print("Floor Division=",x//y)
print("Modulus=",x%y)
print("Exponentiation=",x+y)