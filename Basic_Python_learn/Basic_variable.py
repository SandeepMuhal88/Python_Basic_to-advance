# There is three case of a variable
# 1. Camel Case
# In CamelCase, the variable name starts with a lowercase letter,
# and each subsequent word begins with an uppercase letter.
# No underscores (_) are used.
myname="Sandeep muhal"
print(myname)

# 2.Pascal Case
# Similar to CamelCase, but the first letter of the variable name is also capitalized.
# Each word begins with an uppercase letter.
# Often used for naming classes in Python.
MyName="Pascal case"
print(MyName)

# 3.Snake case
# In Snake_case, all letters are lowercase, and words are separated by underscores (_).
# This is the preferred convention for variables and functions in Python (as per PEP 8, Python's style guide).
my_name="Snake case"
print(my_name)

